class Anime:
    def __init__(self, nombre, genero, episodios):
        self.nombre = nombre
        self.genero = genero
        self.episodios = episodios

    def mostrar_informacion(self):
        print(f'Nombre: {self.nombre}')
        print(f'Genero: {self.genero}')
        print(f'Episodios: {self.episodios}')


